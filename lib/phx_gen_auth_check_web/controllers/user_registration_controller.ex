defmodule PhxGenAuthCheckWeb.UserRegistrationController do
  use PhxGenAuthCheckWeb, :controller

  alias PhxGenAuthCheck.Accounts
  alias PhxGenAuthCheck.Accounts.User
  alias PhxGenAuthCheckWeb.UserAuth

  def new(conn, _params) do
    changeset = Accounts.change_user_registration(%User{})
    render(conn, "new.html", changeset: changeset)
  end

  def create(conn, %{"user" => user_params}) do
    case Accounts.register_user(user_params) do
      {:ok, user} ->
        {:ok, _} =
          Accounts.deliver_user_confirmation_instructions(
            user,
            &Routes.user_confirmation_url(conn, :confirm, &1)
          )

        conn
        |> put_flash(:info, "Hey there, welcome to the platform, we've just sent a confirnation link on your e-mail.")
        |> UserAuth.log_in_user(user)

      {:error, %Ecto.Changeset{} = changeset} ->
        render(conn, "new.html", changeset: changeset)
    end
  end
end
